import os
import pymysql
from dotenv import load_dotenv

load_dotenv()

# 连接时不指定 database，因为要创建新库
conn = pymysql.connect(
    host=os.getenv("MYSQL_HOST", "127.0.0.1"),
    port=int(os.getenv("MYSQL_PORT", 3306)),
    user=os.getenv("MYSQL_USER", "root"),
    password=os.getenv("MYSQL_PASSWORD", ""),
    charset="utf8mb4",
    cursorclass=pymysql.cursors.DictCursor,
)

cursor = conn.cursor()

print("创建 offline_db 数据库...")
cursor.execute("CREATE DATABASE IF NOT EXISTS offline_db DEFAULT CHARACTER SET utf8mb4 DEFAULT COLLATE utf8mb4_unicode_ci")
cursor.execute("USE offline_db")

print("创建 offline_stores 表...")
cursor.execute("DROP TABLE IF EXISTS offline_orders")
cursor.execute("DROP TABLE IF EXISTS offline_stores")
cursor.execute("""
CREATE TABLE offline_stores (
    store_id    INT PRIMARY KEY AUTO_INCREMENT COMMENT '门店ID',
    store_name  VARCHAR(100) NOT NULL COMMENT '门店名称',
    city        VARCHAR(50)  NOT NULL COMMENT '所在城市',
    store_type  VARCHAR(20)  NOT NULL COMMENT '门店类型：flagship旗舰店/partner合作点/kiosk自助机',
    open_date   DATE         NOT NULL COMMENT '开业日期'
) COMMENT='线下门店信息表'
""")

print("创建 offline_orders 表...")
cursor.execute("""
CREATE TABLE offline_orders (
    order_id       INT PRIMARY KEY AUTO_INCREMENT COMMENT '线下订单ID',
    user_id        INT           NOT NULL COMMENT '用户ID（关联hotel_db.users）',
    hotel_id       INT           NOT NULL COMMENT '酒店ID（关联hotel_db.hotels）',
    store_id       INT           NOT NULL COMMENT '成交门店ID',
    city           VARCHAR(50)   NOT NULL COMMENT '城市',
    check_in_date  DATE          NOT NULL COMMENT '入住日期',
    check_out_date DATE          NOT NULL COMMENT '退房日期',
    amount         DECIMAL(10,2) NOT NULL COMMENT '订单金额（元）',
    status         VARCHAR(20)   NOT NULL COMMENT '订单状态：completed/cancelled/refunded',
    consultant     VARCHAR(50)   NOT NULL COMMENT '接待顾问姓名'
) COMMENT='线下门店订单表'
""")

